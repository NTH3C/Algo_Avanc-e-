    /**
     * use the konami code in the game to reverse all the board
     */
    public static void konamiCode(){
        for (int i = 1; i <= 11; i++) {
            for (int j = 1; j <=10 ; j++) {
                //If the case is available destroy it
                if (GameBoard.matrice[i][j]==0){
                    GameBoard.matrice[i][j]=1;
                //If the case is destroyed repair it
                } else if (GameBoard.matrice[i][j]==1) {
                    GameBoard.matrice[i][j]=0;
                }
            }
        }
    }
